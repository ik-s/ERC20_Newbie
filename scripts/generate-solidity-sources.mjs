import { readFile, writeFile, mkdir } from "node:fs/promises";
import { dirname, join, normalize, posix, relative } from "node:path";
import { fileURLToPath } from "node:url";

const root = dirname(fileURLToPath(import.meta.url));
const project = dirname(root);
const packageRoot = join(project, "node_modules", "@openzeppelin", "contracts");
const entries = [
  "token/ERC20/ERC20.sol",
  "token/ERC20/extensions/ERC20Burnable.sol",
  "token/ERC20/extensions/ERC20Capped.sol",
  "access/Ownable.sol",
];
const sources = new Map();

async function visit(packagePath) {
  const normalized = posix.normalize(packagePath).replace(/^\.\//, "");
  if (sources.has(normalized)) return;
  const absolute = join(packageRoot, normalized);
  const content = await readFile(absolute, "utf8");
  sources.set(normalized, content);
  const imports = [...content.matchAll(/import\s+(?:[^"']+from\s+)?["']([^"']+)["']/g)].map((match) => match[1]);
  for (const imported of imports) {
    const next = imported.startsWith(".")
      ? posix.normalize(posix.join(posix.dirname(normalized), imported))
      : imported.replace(/^@openzeppelin\/contracts\//, "");
    await visit(next);
  }
}

for (const entry of entries) await visit(entry);
const output = `// Generated by scripts/generate-solidity-sources.mjs\nexport const openzeppelinSources: Record<string, string> = ${JSON.stringify(Object.fromEntries([...sources].map(([path, content]) => [`@openzeppelin/contracts/${path}`, content])), null, 2)};\n`;
const target = join(project, "src", "generated", "openzeppelinSources.ts");
await mkdir(dirname(target), { recursive: true });
await writeFile(target, output);
console.log(`Generated ${sources.size} whitelisted OpenZeppelin Solidity sources.`);
